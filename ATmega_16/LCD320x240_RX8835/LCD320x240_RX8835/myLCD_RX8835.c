void Rx8835_GoTo(uint16_t y, uint16_t x)
{
	x += y*40;
	Rx8835_WriteCommand(CSRW);
	y = x & 0xFF;
	Rx8835_WriteData(y);
	y = x >> 8;
	Rx8835_WriteData(y);
}

