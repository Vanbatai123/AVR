void Rx8835_Wait()
{
	_delay_ms(1);
}

void Rx8835_SetCursorAddress(uint16_t addr)
{
	uint8_t adress;
	Rx8835_WriteCommand(CSRW);
	adress = addr & 0xFF;
	Rx8835_WriteData(adress);
	adress = addr >> 8;
	Rx8835_WriteData(adress);
}

void Rx8835_FillGraphic(uint16_t parameter)
{
	long count;
	//set cursor to 2580h
	Rx8835_SetCursorAddress(0x2580);
	//put 00h in all graphic space
	count = 9600;
	Rx8835_WriteCommand(0x42);
	while(count != 0)
	{
		Rx8835_WriteData(0xFF * parameter);
		count--;
	}
}

void Rx8835_FillText(char cara)
{
	long count;
	//set cursor to 0000h
	Rx8835_SetCursorAddress(0x0000);
	//put 00h in all text space
	count = 1200;
	Rx8835_WriteCommand(0x42);
	while(count != 0)
	{
		Rx8835_WriteData(cara);
		count--;
	}
}

uint8_t Rx8835_ReadBusyFlag()
{
	uint8_t flag;

	setb(CTRL_PORT, WR);
	clrb(CTRL_PORT, RD);
	clrb(CTRL_PORT, A0);
	
	DATA_PORT = 0xFF;
	DATA_DDR = 0x00; // data pin input
	
	_delay_ms(1);
	flag = DATA_PIN;
	
	setb(CTRL_PORT, RD); //enable
	DATA_DDR = 0xFF;
	return flag;
}

void Rx8835_WriteCommand(uint8_t cmd)
{
	//while(readBusyFlag() & BUSY_FLAG);
	//wait_LCD();
	setb(CTRL_PORT, RD);
	setb(CTRL_PORT, A0);
	clrb(CTRL_PORT, WR);
	
	DATA_PORT = cmd;
	//wait_LCD();
	
	setb(CTRL_PORT, WR);
}

void Rx8835_WriteData(uint8_t dt)
{
	//while(readBusyFlag() & BUSY_FLAG);
	//wait_LCD();

	setb(CTRL_PORT, RD);
	clrb(CTRL_PORT, A0);
	clrb(CTRL_PORT, WR);
	
	DATA_PORT = dt;
	//wait_LCD();
	
	setb(CTRL_PORT, WR);
	
}

uint8_t Rx8835_ReadData()
{
	uint8_t dt;

	setb(CTRL_PORT, WR);
	clrb(CTRL_PORT, RD);
	setb(CTRL_PORT, A0);
	
	DATA_PORT = 0xFF;
	DATA_DDR = 0x00; // data pin input
	
	_delay_ms(1);
	dt = DATA_PIN;
	
	setb(CTRL_PORT, RD); //enable
	DATA_DDR = 0xFF;
	return dt;
}

void Rx8835_Config()
{
	DATA_DDR = 0xFF;
	CTRL_DDR |= (1<<RD) | (1<<WR) | (1<<A0);
	
	clrb(CTRL_PORT, RD);
	clrb(CTRL_PORT, WR);
	clrb(CTRL_PORT, A0);
	
	//System set
	Rx8835_WriteCommand(SYSTEM_SET);// 0x40
	
	//write 8 byte parameter
	Rx8835_WriteData(0x30);// P1: in CGROM, D6 correct, 8 pixel font, single panel, top correction
	Rx8835_WriteData(0x87);// Font width = 5(4+1)
	Rx8835_WriteData(0x07);// Font height = 7(6+1)
	Rx8835_WriteData(0x27);// C/R: 65 character wide
	Rx8835_WriteData(0x2F);// TC/R
	Rx8835_WriteData(0xEF);// L/F: Set the height, in lines, of a frame. The height in lines is equal to L/F + 1, where L/F can range from 0 to 255.
	Rx8835_WriteData(0x28);// PAL: Define the horizontal address range of the virtual screen. APL is the least significant byte of the address.
	Rx8835_WriteData(0x00);// PAH: Define the horizontal address range of the virtual screen. APL is the least significant byte of the address.
	
	//Scroll
	Rx8835_WriteCommand(SCROLL);// 0x44
	/*Set the scroll start address and the number of lines
	per scroll block. Parameters P1 to P10 can be omitted
	if not required. The parameters must be entered
	sequentially as shown in Figure 6-11 page 24*/
	/*    SL: screen lines   */
	/* SAD = L/F +1			*/
	Rx8835_WriteData(0x00);// SAD 1L: ???
	Rx8835_WriteData(0x00);// SAD 1H: ???
	Rx8835_WriteData(0xF0);// SL1:
	Rx8835_WriteData(0x80);// SAD 2L: ???
	Rx8835_WriteData(0x25);// SAD 2H: ???
	Rx8835_WriteData(0xF0);// SL2:
	Rx8835_WriteData(0x00);// SAD 3L: not use
	Rx8835_WriteData(0x4B);// SAD 3H: not use
	Rx8835_WriteData(0x00);// SAD 4L: not use
	Rx8835_WriteData(0x00);// SAD4H: not use
	
	
	//HDOTSCR
	Rx8835_WriteCommand(HDOT_SCR);
	Rx8835_WriteData(0x00);// scroll 0 line
	Rx8835_Wait();
	
	//Ovlay
	Rx8835_WriteCommand(OVLAY);
	Rx8835_WriteData(0x01);// OR mode
	
	
	//erase all screen
	Rx8835_FillGraphic(0);
	//println_UART("graphic done");
	
	Rx8835_FillText(' ');
	
	//println_UART("fill text done");
	
	//Display Off
	Rx8835_WriteCommand(DISPLAY_OFF);
	Rx8835_WriteData(0x56);
	Rx8835_Wait();
	
	Rx8835_WriteCommand(CSRFORM);
	Rx8835_WriteData(0x04);// scroll 0 line
	Rx8835_WriteData(0x86);// scroll 0 line
	
	//Display on
	Rx8835_WriteCommand(DISPLAY_ON);
	Rx8835_Wait();
	
	
	//CSDIR
	Rx8835_WriteCommand(0x4C);
	
	//CSRW
	Rx8835_SetCursorAddress(0x0000);
}

